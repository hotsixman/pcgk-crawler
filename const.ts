function getGOODs_NAME() {
    const p = document.querySelector('.select2-results__options');
    if (!p) return null;
    return [...new Set(Array.from(p.querySelectorAll(':scope > li')).flatMap((e) => {
        return Array.from(e.querySelectorAll(':scope > ul > li')).map((el) => {
            return (el as HTMLElement).innerText
        })
    }))]
}

